package oefeningen.oefening1;

public class Toren {

    private String naam;
    private String locatie;
    private int voltooid;
    private double hoogteZonderAntenne;
    private int verdiepingen;

    public String getNaam() {
        return naam;
    }

    public void setNaam(String naam) {
        this.naam = naam;
    }

    public String getLocatie() {
        return locatie;
    }

    public void setLocatie(String locatie) {
        this.locatie = locatie;
    }

    public int getVoltooid() {
        return voltooid;
    }

    public void setVoltooid(int voltooid) {
        this.voltooid = voltooid;
    }

    public double getHoogteZonderAntenne() {
        return hoogteZonderAntenne;
    }

    public void setHoogteZonderAntenne(double hoogteZonderAntenne) {
        this.hoogteZonderAntenne = hoogteZonderAntenne;
    }

    public int getVerdiepingen() {
        return verdiepingen;
    }

    public void setVerdiepingen(int verdiepingen) {
        this.verdiepingen = verdiepingen;
    }
}
